// Function to copy options from one select element to another
function copyOptions(fromSelect, toSelect) {
  // Clear existing options
  toSelect.innerHTML = "";

  // Clone each option from the original select
  Array.from(fromSelect.options).forEach((option) => {
    const clonedOption = option.cloneNode(true);
    toSelect.appendChild(clonedOption);
  });
}

