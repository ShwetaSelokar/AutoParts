function updateModels1() {
  // Get the selected brand
  const brand = document.getElementById("car-brand1").value;
  const modelSelect = document.getElementById("car-model1");
  const othersTextArea = document.getElementById("others-textbox");

  if (brand === "others") {
    othersTextArea.removeAttribute("hidden");
    document.getElementById("car-model1").setAttribute("disabled", "");
    document.getElementById("parts1").setAttribute("disabled", "");
  }

  // Clear the existing options in the model dropdown

  // If a brand is selected, populate the models
  if (brand && models1[brand]) {
    models1[brand].forEach((model) => {
      const option = document.createElement("option");
      option.value = model.toLowerCase().replace(/ /g, "-");
      option.textContent = model;
      modelSelect.appendChild(option);
    });
  }
}

